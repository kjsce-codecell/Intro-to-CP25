//Basic Data Types

#include <bits/stdc++.h>
using namespace std;

int main(){
      
      int var = 10;
  
      cout << var << "\n";

      float var1 = 10.5;
      cout << var1 << "\n";

      double var2 = 1023435425;
      cout << var2 <<"\n";

      char var3 = 'a';
      cout << var3 <<"\n";

      bool var4 = true;
      cout << var4 <<"\n";

      string var5 = "Hello World";
      cout << var5;

      return 0;
      // Point to Remember: Use "\n" for new line. You can also use "\n" for new line.
}

